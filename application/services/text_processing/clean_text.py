import re

def clean_text(text: str) -> str:
    # Удаляем всё, кроме нужного (и \n)
    text = re.sub(r"[^a-zA-Zа-яА-Я0-9\s.,:;!?-\[\]\n]", " ", text)

    # Сжимаем повторяющиеся пробелы (но НЕ трогаем \n)
    text = re.sub(r"[ \t]+", " ", text)  # только обычные пробелы и табы

    # Удаляем лишние пробелы вокруг \n
    text = re.sub(r" *\n *", "\n", text)

    # Удаляем лишние \n подряд (опционально, если хочешь максимум два подряд)
    text = re.sub(r"\n{3,}", "\n\n", text)

    return text.strip()
